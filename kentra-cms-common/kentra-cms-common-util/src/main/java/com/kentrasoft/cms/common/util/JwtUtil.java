package com.kentrasoft.cms.common.util;

import io.jsonwebtoken.Claims;
import io.jsonwebtoken.Jwts;
import io.jsonwebtoken.SignatureAlgorithm;
import java.io.Serializable;
import java.util.Date;
import java.util.Map;

/**
 * Jwt工具类
 */
public final class JwtUtil implements Serializable {

    /**
     * 从数据声明生成令牌
     *
     * @param claims 数据声明
     * @return 令牌
     */
    public static String generateToken(Map<String, Object> claims,String secret,Long extTime) {
        Date expirationDate = new Date(System.currentTimeMillis() + extTime);
        return Jwts.builder().setClaims(claims).setExpiration(expirationDate).signWith(SignatureAlgorithm.HS512, secret).compact();
    }

    /**
     * 从令牌中获取数据声明
     *
     * @param token 令牌
     * @return 数据声明
     */
    public static Claims getClaimsFromToken(String token,String secret) {
        Claims claims;
        try {
            claims = Jwts.parser().setSigningKey(secret).parseClaimsJws(token).getBody();
        } catch (Exception e) {
            claims = null;
        }
        return claims;
    }

    /**
     * 从令牌中获取用户名
     *
     * @param token 令牌
     * @return 用户名
     */
    public static String getUsernameFromToken(String token,String secret) {
        String username;
        try {
            Claims claims = getClaimsFromToken(token,secret);
            username = claims.getSubject();
        } catch (Exception e) {
            username = null;
        }
        return username;
    }

    /**
     * 判断令牌是否过期
     *
     * @param token 令牌
     * @return 是否过期
     */
    public static Boolean isTokenExpired(String token,String secret) {
        try {
            Claims claims = getClaimsFromToken(token,secret);
            Date expiration = claims.getExpiration();
            return expiration.before(new Date());
        } catch (Exception e) {
            return false;
        }
    }

    /**
     * 刷新令牌
     *
     * @param token 原令牌
     * @return 新令牌
     */
    public static String refreshToken(String token,String secret,Long extTime) {
        String refreshedToken;
        try {
            Claims claims = getClaimsFromToken(token,secret);
            claims.put("created", new Date());
            refreshedToken = generateToken(claims,secret,extTime);
        } catch (Exception e) {
            refreshedToken = null;
        }
        return refreshedToken;
    }
}
